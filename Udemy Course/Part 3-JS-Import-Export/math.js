const pi = 3.142;

function doublePi() {
  return 2 * pi;
}

function triplePi() {
  return 3 * pi;
}

function add(a, b) {
  return a + b;
}

function multiply(a, b) {
  return a * b;
}

function subtract(a, b) {
  return a - b;
}

function divide(a, b) {
  return a / b;
}

export default pi;
export { doublePi, triplePi, add, multiply, subtract, divide };
